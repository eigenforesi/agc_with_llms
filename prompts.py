import json
from pathlib import Path

# Prompt class - this defines an object for the individual prompt
class Prompt:
    # Initialize
    def __init__(self, index: str, query: str, output: str, errlog: str, returncode: int, success: bool):
        self.id = index # unique identifier for this particular prompt
        self.query = query # the query passed into the LLM
        self.output = output # the output generated by the LLM in response to the query
        self.errlog = errlog # the error log generated by the LLM in response to the query
        self.returncode = returncode # the return code of the prompt
        self.success = success # boolean, whether the generated code runs successfully
    
    # truncate the query to show just the beginning, truncate the output, display stats
    def printPrompt(self, q_size: int = 100, o_size: int = 100, e_size: int = 100):
        query_short = (self.query[:q_size] + '..') if len(self.query) > q_size else self.query
        output_short = (self.output[:o_size] + '..') if len(self.output) > o_size else self.output
        error_short = (self.errlog[:e_size] + '..') if len(self.errlog) > e_size else self.errlog
        print("Prompt ID: " + self.id)
        print("Query: " + query_short)
        print("Output log: " + output_short)
        print("Error log: " + error_short)
        print("Success: " + str(self.success))
    
    # returns boolean describing whether the code runs or not
    def getSuccess(self):
        return self.success
    
    # returns the output log of the prompt (should be a string)
    def getOutput(self):
        return self.output
        
